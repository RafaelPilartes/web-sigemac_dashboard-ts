import axios, { AxiosResponse } from 'axios'
import { ApiJasei } from '../api.jasei'
import { NewsInterface } from '../../../interfaces/news'
import { ApiResponseDataInterface } from '../../../interfaces/IApiResponse'

const route = 'news'

export async function getAllNews(): Promise<
  AxiosResponse<ApiResponseDataInterface>
> {
  return new Promise(async (resolve, reject) => {
    const url = `${ApiJasei}/${route}/get/all`
    axios
      .get(url)
      .then(resData => {
        resolve(resData as any)
      })
      .catch(error => {
        reject(error)
      })
  })
}
export async function getAllNewsByTerm(
  term: string
): Promise<AxiosResponse<ApiResponseDataInterface>> {
  return new Promise(async (resolve, reject) => {
    const url = `${ApiJasei}/${route}/search/all/${term}`
    if (term) {
      axios
        .get(url)
        .then(resData => {
          resolve(resData as any)
        })
        .catch(error => {
          reject(error)
        })
    }
  })
}

export async function getNews(email: string): Promise<NewsInterface> {
  return new Promise(async (resolve, reject) => {
    const url = `${ApiJasei}/${route}/get/`
    if (email) {
      axios
        .post(url, email)
        .then(resData => {
          resolve(resData as any)
        })
        .catch(error => {
          reject(error)
        })
    }
  })
}

